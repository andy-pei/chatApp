    public function update(Request $request)
    {
        $file = Input::file('file_url');
        if($file->isValid()) {
            $file->move(public_path().'/images', $file->getClientOriginalName());
        }
        $data = array_except(Input::all(), ['_token', 'file_url']);
        $data['icon_url'] = 'http://chat.dev/images/' . $file->getClientOriginalName();
        $id = Auth::user()->id;
        $this->userService->editUser($id, $data);
        return redirect('/');
    }

